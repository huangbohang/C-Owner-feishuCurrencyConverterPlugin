export function isValidCurrencyPair(pair: string): {
  valid: boolean;
  list: string[];
} {
  // 正则表达式用于匹配格式：货币代码:货币代码
  const regex = /^(\w{3}):(\w{3})$/;

  // 检查字符串是否符合正则表达式
  if (!regex.test(pair)) {
    return { valid: false, list: [] };
  }

  // 分割字符串为两个货币代码
  const [currency1, currency2] = pair.split(":");

  // 检查两个货币代码是否都在支持的货币列表中
  if (
    SUPPORTED_CURRENCY.includes(currency1) &&
    SUPPORTED_CURRENCY.includes(currency2)
  ) {
    return { valid: true, list: [currency1, currency2] };
  } else {
    return { valid: false, list: [] };
  }
}
